export type TranslationKey = keyof typeof translations.en;

interface I18nStore {
  locale: Locale;
  setLocale: (locale: Locale) => void;
  t: (key: TranslationKey) => string;
}

export const useI18n = create<I18nStore>((set, get) => ({
  locale: (navigator.language.startsWith('tr') ? 'tr' : 'en') as Locale,

  setLocale: (locale) => {
    set({ locale });
    localStorage.setItem('focus-universe-locale', locale);
  },

  t: (key) => {
    const locale = get().locale;
    return translations[locale][key] ?? translations.en[key] ?? key;
  },
}));

const savedLocale = localStorage.getItem('focus-universe-locale') as Locale | null;
if (savedLocale && (savedLocale === 'en' || savedLocale === 'tr')) {
  useI18n.setState({ locale: savedLocale });
}
